/**
 * Represents the type of health that is assigned to a player.
 *
 * This enum is for REPENTOGON, an exe-hack which expands the modding API and should not be confused
 * with Isaacscript Common's `HealthType` enum.
 *
 * @see https://repentogon.com/index.html
 */
export enum RepentogonHealthType {
  RED = 0,
  SOUL = 1,
  NO_HEALTH = 2,
  COIN = 3,
  BONE = 4,
}
